package primitives;

public class Arithmetic {
   public static void main (String[] args) {
	   
	   int a = 7;
	   int b = 3;
	   System.out.println(a + b);
	   System.out.println(a - b);
	   System.out.println(a * b);
	   System.out.println(a % b);
	   System.out.println(a / b);
//		7 % 3
//	    7 / 3
//	    7 3 and 3
//	    7 - 6 = 1
//	    7 % 3 = 1   
   }
}      
