package Arrayss;
import java.util.Arrays;
public class forArrays {
public static void main(String[] args) {
	String[] color = {"Blue", "Green", "Yellow", "red"};
	int size = color.length;
	for(int i = 0 ; i < size; i++) {
		if(color[i].length() >= 4) {
		System.out.println(color[i]);
		}
	}
	
	
			
}
}
